export function slackEmojiToUnicode(slackName: string): string {
  const cleaned = slackName
    .replace(/^:/, "")
    .replace(/:$/, "")
    .replace(/::skin-tone-\d/, "");

  if (SLACK_EMOJI_MAP[cleaned]) return SLACK_EMOJI_MAP[cleaned];
  if (CUSTOM_EMOJI_MAP[cleaned]) return CUSTOM_EMOJI_MAP[cleaned];

  // Handle skin tone variants: e.g. "+1::skin-tone-3" -> 👍
  const baseName = cleaned.replace(/_skin_tone_\d/, "").replace(/-skin-tone-\d/, "");
  if (SLACK_EMOJI_MAP[baseName]) return SLACK_EMOJI_MAP[baseName];
  if (CUSTOM_EMOJI_MAP[baseName]) return CUSTOM_EMOJI_MAP[baseName];

  return `:${cleaned}:`;
}
